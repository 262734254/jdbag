package com.xiu.jd.bean.ware;

import java.io.Serializable;

/**
 * 商品价格变化jms
 * @author liweibao 
 *
 */
public class ActivityPriceJms implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = -714855324237287641L;
	/**商品走秀码**/
	private String goodSn;
	/**商品活动类型**/
	private String activityType;
	/**商品活动价格**/
	private String activityPrice;
	/**商品活动价格类型**/
	private String activityPriceType;
	
	private String saleCatIds;
	
	/**商品走秀价格**/
	private String xiuPrice;
	
	/**商品所属品牌ID**/
	private String brandId;
	
	/**商品所属分类ID(四级分类ID)**/
	private String catId;

	public String getGoodSn() {
		return goodSn;
	}

	public void setGoodSn(String goodSn) {
		this.goodSn = goodSn;
	}

	public String getActivityType() {
		return activityType;
	}

	public void setActivityType(String activityType) {
		this.activityType = activityType;
	}

	public String getActivityPrice() {
		return activityPrice;
	}

	public void setActivityPrice(String activityPrice) {
		this.activityPrice = activityPrice;
	}
	
	

	public String getActivityPriceType() {
		return activityPriceType;
	}

	public void setActivityPriceType(String activityPriceType) {
		this.activityPriceType = activityPriceType;
	}
	
	

	public String getSaleCatIds() {
		return saleCatIds;
	}

	public void setSaleCatIds(String saleCatIds) {
		this.saleCatIds = saleCatIds;
	}

	public String getXiuPrice() {
		return xiuPrice;
	}

	public void setXiuPrice(String xiuPrice) {
		this.xiuPrice = xiuPrice;
	}

	public String getBrandId() {
		return brandId;
	}

	public void setBrandId(String brandId) {
		this.brandId = brandId;
	}

	public String getCatId() {
		return catId;
	}

	public void setCatId(String catId) {
		this.catId = catId;
	}

	@Override
	public String toString() {
		return "ActivityPrice [goodSn=" + goodSn + ", activityType="
				+ activityType + ", activityPrice=" + activityPrice
				+ ", activityPriceType=" + activityPriceType + ", saleCatIds="
				+ saleCatIds + ", xiuPrice=" + xiuPrice + ", brandId="
				+ brandId + ", catId=" + catId + "]";
	}


	

	
	
	
	

}
