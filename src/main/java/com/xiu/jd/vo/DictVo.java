package com.xiu.jd.vo;

import java.io.Serializable;

public class DictVo implements Serializable{
	private static final long serialVersionUID = -3893338525028711942L;

	private String id;
	private String name;
	public DictVo() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
